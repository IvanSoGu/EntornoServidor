<?php

    function suma($num1,$num2){

        return ("$num1 + $num2 = ".($num1+$num2));

    }

    function resta($num1,$num2){

        return ("$num1 - $num2 = ".($num1-$num2));

    }

    function multi($num1,$num2){

        return ("$num1 * $num2 = ".($num1*$num2));

    }

    function div($num1,$num2){

        return ("$num1 / $num2 = ".($num1/$num2));

    }

    function modulo($num1,$num2){

        return ("$num1 % $num2 = ".($num1%$num2));

    }

    function potencia($num1,$num2){

        return ("$num1 ^ $num2 = ".($num1**$num2));

    }

?>